print("A car is traveling 70 miles per hour.")
print(" To get the distance, you must multiply the time by the speed.")
speed = int(70)

# Follow the formula: distance = speed * time  
distanceAfter6 = int(speed * 6)
distanceAfter10 = int(speed * 10)
distanceAfter15 = int(speed * 15)

# Display results
print ("The distance the car will travel in 6 hours is: ", distanceAfter6)
print ("The distance the car will travel in 10 hours is: ", distanceAfter10)
print ("The distance the car will travel in 15 hours is: ", distanceAfter15)






